const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const urlValidation = require('../../validations/url.validation');
const urlController = require('../../controllers/url.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUrls'), validate(urlValidation.createUrl), urlController.createUrl)
  .get(auth('getUrls'), validate(urlValidation.getUrls), urlController.getUrls);

router
  .route('/:urlId')
  .get(auth('getUrls'), validate(urlValidation.getUrl), urlController.getUrl)
  .patch(auth('manageUrls'), validate(urlValidation.updateUrl), urlController.updateUrl)
  .delete(auth('manageUrls'), validate(urlValidation.deleteUrl), urlController.deleteUrl);

module.exports = router;

